// MARK: -Import Libaries
import Foundation
import UIKit

// MARK: -Custom Font
extension UIFont {
    class func fontSFProDisplay(size: CGFloat) -> UIFont {
        return UIFont.init(name: "SFProDisplay-Regular", size: size)!
     }
}
